def plot_kernel_matrix(K, title="Kernel Matrix", save_path=None):
    plt.figure()
    plt.imshow(K.detach().cpu().numpy(), aspect="auto")
    plt.colorbar()
    plt.title(title)

    if save_path:
        plt.savefig(save_path, dpi=300)

    plt.show()


# ======================================================
# 3. REGRESSION RESULT (MAIN PLOT)
# ======================================================

def plot_regression(
    X_train,
    y_train,
    X_test,
    y_test,
    pred_mean,
    pred_var,
    save_path=None
):

    X_train = X_train.detach().cpu()
    y_train = y_train.detach().cpu()

    X_test = X_test.detach().cpu()
    y_test = y_test.detach().cpu()

    mean = pred_mean.detach().cpu()
    std = torch.sqrt(pred_var.detach().cpu())

    lower = mean - std
    upper = mean + std

    fig, ax = plt.subplots(figsize=(5.0, 4.0))

    ax.plot(
        X_train.numpy(),
        y_train.numpy(),
        "k.",
        label="Samples",
        markersize=8.0
    )

    ax.plot(
        X_test.numpy(),
        mean.numpy(),
        label="Mean"
    )

    ax.plot(
        X_test.numpy(),
        y_test.numpy(),
        color="k",
        label="True"
    )

    ax.fill_between(
        X_test.numpy(),
        lower.numpy(),
        upper.numpy(),
        alpha=0.3,
        label="Variance"
    )

    ax.plot(
        X_test.numpy(),
        lower.numpy(),
        "k--",
        linewidth=1.5
    )

    ax.plot(
        X_test.numpy(),
        upper.numpy(),
        "k--",
        linewidth=1.5
    )

    ax.set_xlim([-1.0, 1.0])
    ax.set_ylim([-1.5, 1.5])

    ax.set_xlabel(r'$\mathbf{x}$')
    ax.set_ylabel(r'$f(\mathbf{x})$')

    ax.legend(loc="best")
    ax.grid()

    plt.tight_layout()

    if save_path:
        plt.savefig(save_path)

    plt.show()


