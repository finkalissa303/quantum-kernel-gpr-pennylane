# 4. PREDICTION UNCERTAINTY ONLY (OPTIONAL DEBUG TOOL)
# ======================================================

def plot_uncertainty(X, mean, var):
    X = X.detach().cpu()
    mean = mean.detach().cpu()
    std = torch.sqrt(var.detach().cpu())

    plt.figure()
    plt.plot(X, mean, label="Mean")
    plt.fill_between(
        X.numpy(),
        (mean - std).numpy(),
        (mean + std).numpy(),
        alpha=0.3
    )
    plt.legend()
    plt.title("Uncertainty")
    plt.show()


