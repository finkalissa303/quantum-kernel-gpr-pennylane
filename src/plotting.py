def plot_losses_and_gradients(losses, grad_norms=None):
    plt.figure()
    plt.plot(losses, label="Loss")

    if grad_norms is not None:
        plt.plot(grad_norms, label="Grad norm")

    plt.legend()
    plt.title("Training Diagnostics")
    plt.show()

